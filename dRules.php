	<div class="container">
  		<h1>Dodgeball Rules</h1>    
  			<h2><p>The Court</p></h2>
			<ul class="list-inline">
  				<h4> <li><ul class="list-inline">
  				<h4><li>&bull; The official dimensions for a regulation court are as follows:</li></h4>
				<h4><li>&bull; The court is divided into two 30' X 30' areas, with a 4' X 30' neutral zone located at center court separating the two sides, an attack line located parallel and 10' from the center line, for a total court length of 60' from endline to endline, and a total width of 30' from sideline to sideline.</h4></li>
  				<h4><li>&bull; Approximately 2'-3' should be allotted for an out of bound area, allowing officials to move freely along the sidelines.</li></4h>
  				<h4><li>&bull; The Queue for each team is a 3' X 12' area, and should be located 2'-3' from the sideline, leaving enough room for an official to move free along the sideline.</li></h4>
  				<h4><li>&bull; Every effort should be made to obtain the correct dimensions. However court size may be adjusted to best suit the available space. </h4></li>
			</ul>
			<h2>Equipment</h2>
			<ul class="list-inline">
				<h4> <li><ul class="list-inline">
				<h4><li>&bull; 6 regulation size balls should be used: 4 Blockers™ (8.5") and 2 Stingers™ (5"). </h4></li>
				<h4><li>&bull; Ball layout goes as follows: Blocker, Stinger, Blocker, Blocker, Stinger, Blocker.</h4></li>
			</ul>

			<h2>Players</h2>
                        <ul class="list-inline">
				<h4><li>&bull; Teams consist of six players with up to four substitutes. All players must be in uniform.</h4></li>
				<h4><li>&bull; Coed teams may consist of either gender, however at least two players of each gender must participate at the start of each game.</h4></li>
				<h4><li>&bull; Open teams may consist of either gender. Any ratio of men and women may participate, including all male and all female teams.</h4></li>
			</ul>



			<h2>Substitutions</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; Substitutions must be made prior to the start of the game. No substitutions can be made during a game, except in cases of injury.</h4></li>
                        </ul>

			<h2>Retrievers</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; Retrievers are individuals designated to retrieve balls that go out of play. Teams are responsible for providing retrievers. The number of retrievers required will determined by the tournament director.</h4></li>
                                <h4><li>&bull; Or you may use players that are out as retrievers.</h4></li>
                                <h4><li>&bull; Retrievers may not enter the court at any time.</h4></li>
                                <h4><li>&bull; Retrievers may not wear jerseys of the same style as their team uniform.</h4></li>
				<h4><li>&bull; Retrievers are only allowed to field balls from their side of the court.</h4></li>
			</ul>

			<h2>Matches</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; A regulation match consists of a pre-determined odd number of games of a single game type (best of 3 games, best of 5 games in championship, only). </h4></li>
                                <h4><li>&bull; The number of games and/or the time allotted may be altered to best suit time and attendance.</h4></li>
                        </ul>



		<h2><p>Game Types:</p></h2>
			<h2>Elimination Game</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; A game played until all opponents on one side have been eliminated. The first team to eliminate all its opponents is declared the winner.</h4></li>
                        </ul>
			
			<h2>Timed Game (5 mins)</h2>
                        <ul class="list-inline">
                              <h4><li>&bull; A game played until pre-determined time limit expires or all opponents are eliminated on one side. If time expires, all remaining players are counted. The team with most players remaining wins the game.</h4</li>
                              <h4><li>&bull; If both teams are left with the same number of players at the end of the timed game, the balls will be reset and a sudden death game will ensue.</h4></li>
                        </ul>

                        <h2>Beginning Play</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; Play begins with all players positioned behind their team's endline. </h4></li>
                        </ul>


                        <h2>The Rush</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; The Rush occurs at the beginning of each game or reset.</h4></li>
                       		<h4><li>&bull; Upon the official's signal, both teams rush to center court and attempt to retrieve as many balls as possible.</h4></li>
				<h4><li>&bull; A team may rush with as many or as few players as it wants, but at least one person from each team has to Rush.</h4></li>
 				<h4><li>&bull; There is no limit to how many balls an individual player may retrieve.</h4></li>
 				<h4><li>&bull; Players may not slide or dive head first into the neutral zone or they will be called out.</h4></li>
 				<h4><li>&bull; Crossing over the neutral zone will result in an "out."</h4></li>
                                <h4><li>&bull; Players may not physically grab and pull another player across the neutral zone or prevent them from returning to their side of the court.</h4></li>

                        </ul>

                        <h2>Putting a Ball in Play</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; The player and the ball must go completely behind the attack line. During the Rush, any ball retrieved from the neutral zone must be returned behind the attack line before it may be thrown at an opponent. A ball that hasn't crossed the attack line is considered a dead ball, any hits are voided plays.</h4></li>
                                <h4><li>&bull; There are several ways to put a ball into play following a Rush.</h4></li>
				<h4><li>&bull; A player carries the ball across the attack line.</h4></li>
				<h4><li>&bull; A player passes the ball a teammate who is behind or carries it across the attack line.</h4></li>
				<h4><li>&bull; A player rebounds the ball off the back wall of a closed court.</h4></li>
                        </ul>

                        <h2>Time Outs</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; There are no team time outs</h4></li>
                        </ul>
    

                       <h2>Outs</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; player shall be deemed "out" when a live ball hits any part of the player's body, clothing, or uniform.</h4></li>
                                <h4><li>&bull; If a player is hit by a live ball rebounding off another player.</h4></li>
				<h4><li>&bull; A defending player catches a live ball they have thrown.</h4></li>
				<h4><li>&bull; If a player is steps or is forced out of bounds from a thrown ball.</h4></li>
                                <h4><li>&bull; Players shall return from the Queue in the order they were put "out".(unless there are no females, must have female enter 1st).</h4></li>

                        </ul>

                        <h2>Blocking</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; Players can defend themselves by blocking the ball in flight with another ball but must retain control over the ball they are blocking with. A player dropping or losing possession of the blocking ball is deemed "out."</h4></li>
                                <h4><li>&bull; Any blocked ball rebounding off another ball is considered live. Any player hit by the rebounding ball is deemed "out."</h4></li>
                        </ul>

                        <h2>Stalling</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; The act of intentionally delaying the game.</h4></li>
                                <h4><li>&bull; If a referee determines that a player or team is stalling, the referee will warn player or team. If the stalling continues, at the referee's discretion, player or team will lose possession of all balls on their side.</h4></li>
                                <h4><li>&bull; If both teams are stalling a reset should occur.</h4></li>

                        </ul>

                        <h2>Out of Bounds Rule</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; If any part of the player's body touches the endlines or far neutral zone line, the player shall be deemed "out".</h4></li>
                                <h4><li>&bull; Momentum may carry a player out of bounds while making a catch. Providing control of the ball was established prior going out of bounds.</h4></li>
                        </ul>

                        <h2>Neutral Zone Rule</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; The neutral zone is a 4' by 30' area centered around the centerline. A player may safely step into the neutral zone but not across. Any player crossing over the neutral zone is deemed "out."</h4></li>
                        </ul>


                        <h2>Sacrifice Fly</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; An airborne attack, where an attacking player may legally cross the neutral zone to hit an opponent but the ball must leave the attacker's hand before any part of the attacker's body touches the opponent's territory. If successful, the player hit is out and attacking player remains in.</h4></li>
                        </ul>

                        <h2>Headshots</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; A headshot occurs when a player is hit directly in the head by a high thrown ball.</h4></li>
                                <h4><li>&bull; Any player that gets hit in the head, unless in the act of dodging, will remain in the game.</h4></li>
				<h4><li>&bull; Any thrower committing a headshot will be deemed "out."</h4></li>
				<h4><li>&bull; Any thrower that commits this penalty will out for the rest of the match if the thrower commits this foul again the thrower will be out for the rest of the tournament.</h4></li>
                        </ul>

                        <h2>Uniforms</h2>
                        <ul class="list-inline">
                                <h4><li>&bull; The NDL does not allow players to wear gloves, or baseball caps during play.</h4></li> 
                                <h4><li>&bull; Each team should wear the same color uniform.</h4></li>
 	                </ul>

